CREATE PROCEDURE UPDVIEWCOL(  
  IN DOC CLOB,
  IN IMPORT SMALLINT)

RESULT SETS 0
LANGUAGE SQL
DISABLE DEBUG MODE 

BEGIN
 DECLARE PDOC XML;
 SET PDOC = XMLPARSE(DOCUMENT DOC PRESERVE WHITESPACE);

-- If normal update
 IF IMPORT = 0 THEN

  FOR OP AS SELECT X.*
   FROM XMLTABLE ('$doc/Root/Record' passing PDOC as "doc" 
    COLUMNS
     "ENVIRONID"          INT PATH 'ENVIRONID',
     "VIEWCOLUMNID"       INT PATH 'VIEWCOLUMNID',
     "VIEWID"             INT PATH 'VIEWID',
     "COLUMNNUMBER"       INT PATH 'COLUMNNUMBER',
     "FLDFMTCD"           CHAR(5) PATH 'FLDFMTCD',
     "SIGNEDIND"          SMALLINT PATH 'SIGNEDIND',
     "STARTPOSITION"      INT PATH 'STARTPOSITION',
     "MAXLEN"             INT PATH 'MAXLEN',
     "ORDINALPOSITION"    INT PATH 'ORDINALPOSITION',
     "DECIMALCNT"         INT PATH 'DECIMALCNT',
     "ROUNDING"           INT PATH 'ROUNDING',
     "FLDCONTENTCD"       CHAR(5) PATH 'FLDCONTENTCD',
     "JUSTIFYCD"          CHAR(5) PATH 'JUSTIFYCD',
     "DEFAULTVAL"         VARCHAR(254) PATH 'DEFAULTVAL',
     "VISIBLE"            SMALLINT PATH 'VISIBLE',
     "SUBTOTALTYPECD"     CHAR(5) PATH 'SUBTOTALTYPECD',
     "SPACESBEFORECOLUMN" INT PATH 'SPACESBEFORECOLUMN',
     "EXTRACTAREACD"      CHAR(5) PATH 'EXTRACTAREACD',
     "EXTRAREAPOSITION"   INT PATH 'EXTRAREAPOSITION',
     "SUBTLABEL"          VARCHAR(48) PATH 'SUBTLABEL',
     "RPTMASK"            VARCHAR(48) PATH 'RPTMASK',
     "HDRJUSTIFYCD"       CHAR(5) PATH 'HDRJUSTIFYCD',
     "HDRLINE1"           VARCHAR(254) PATH 'HDRLINE1',
     "HDRLINE2"           VARCHAR(254) PATH 'HDRLINE2',
     "HDRLINE3"           VARCHAR(254) PATH 'HDRLINE3',
     "FORMATCALCLOGIC"    CLOB(32 M) PATH 'FORMATCALCLOGIC',
     "LASTMODUSERID"      VARCHAR(8) PATH 'LASTMODUSERID') AS X
  DO    
    UPDATE VIEWCOLUMN SET
     COLUMNNUMBER=OP.COLUMNNUMBER,
     FLDFMTCD=OP.FLDFMTCD,SIGNEDIND=OP.SIGNEDIND,
     STARTPOSITION=OP.STARTPOSITION,MAXLEN=OP.MAXLEN,
     ORDINALPOSITION=OP.ORDINALPOSITION,DECIMALCNT=OP.DECIMALCNT,
     ROUNDING=OP.ROUNDING,FLDCONTENTCD=OP.FLDCONTENTCD,
     JUSTIFYCD=OP.JUSTIFYCD,DEFAULTVAL=OP.DEFAULTVAL,
     VISIBLE=OP.VISIBLE,SUBTOTALTYPECD=OP.SUBTOTALTYPECD,
     SPACESBEFORECOLUMN=OP.SPACESBEFORECOLUMN,
     EXTRACTAREACD=OP.EXTRACTAREACD,
     EXTRAREAPOSITION=OP.EXTRAREAPOSITION,
     SUBTLABEL=OP.SUBTLABEL,RPTMASK=OP.RPTMASK,
     HDRJUSTIFYCD=OP.HDRJUSTIFYCD,HDRLINE1=OP.HDRLINE1,
     HDRLINE2=OP.HDRLINE2,HDRLINE3=OP.HDRLINE3,
     FORMATCALCLOGIC=OP.FORMATCALCLOGIC,
     LASTMODUSERID=OP.LASTMODUSERID,
     LASTMODTIMESTAMP=CURRENT TIMESTAMP
    WHERE ENVIRONID=OP.ENVIRONID
    AND VIEWID=OP.VIEWID
    AND VIEWCOLUMNID=OP.VIEWCOLUMNID;
  END FOR;

 -- Update from an import or migration
 ELSE

  FOR OP AS SELECT X.*
   FROM XMLTABLE ('$doc/Root/Record' passing PDOC as "doc" 
    COLUMNS
     "ENVIRONID"          INT PATH 'ENVIRONID',
     "VIEWCOLUMNID"       INT PATH 'VIEWCOLUMNID',
     "VIEWID"             INT PATH 'VIEWID',
     "COLUMNNUMBER"       INT PATH 'COLUMNNUMBER',
     "FLDFMTCD"           CHAR(5) PATH 'FLDFMTCD',
     "SIGNEDIND"          SMALLINT PATH 'SIGNEDIND',
     "STARTPOSITION"      INT PATH 'STARTPOSITION',
     "MAXLEN"             INT PATH 'MAXLEN',
     "ORDINALPOSITION"    INT PATH 'ORDINALPOSITION',
     "DECIMALCNT"         INT PATH 'DECIMALCNT',
     "ROUNDING"           INT PATH 'ROUNDING',
     "FLDCONTENTCD"       CHAR(5) PATH 'FLDCONTENTCD',
     "JUSTIFYCD"          CHAR(5) PATH 'JUSTIFYCD',
     "DEFAULTVAL"         VARCHAR(254) PATH 'DEFAULTVAL',
     "VISIBLE"            SMALLINT PATH 'VISIBLE',
     "SUBTOTALTYPECD"     CHAR(5) PATH 'SUBTOTALTYPECD',
     "SPACESBEFORECOLUMN" INT PATH 'SPACESBEFORECOLUMN',
     "EXTRACTAREACD"      CHAR(5) PATH 'EXTRACTAREACD',
     "EXTRAREAPOSITION"   INT PATH 'EXTRAREAPOSITION',
     "SUBTLABEL"          VARCHAR(48) PATH 'SUBTLABEL',
     "RPTMASK"            VARCHAR(48) PATH 'RPTMASK',
     "HDRJUSTIFYCD"       CHAR(5) PATH 'HDRJUSTIFYCD',
     "HDRLINE1"           VARCHAR(254) PATH 'HDRLINE1',
     "HDRLINE2"           VARCHAR(254) PATH 'HDRLINE2',
     "HDRLINE3"           VARCHAR(254) PATH 'HDRLINE3',
     "FORMATCALCLOGIC"    CLOB(32 M) PATH 'FORMATCALCLOGIC',
     "CREATEDTIMESTAMP"   TIMESTAMP PATH 'CREATEDTIMESTAMP',
     "CREATEDUSERID"      VARCHAR(8) PATH 'CREATEDUSERID',
     "LASTMODTIMESTAMP"   TIMESTAMP PATH 'LASTMODTIMESTAMP',
     "LASTMODUSERID"      VARCHAR(8) PATH 'LASTMODUSERID') AS X
  DO    
    UPDATE VIEWCOLUMN SET
     COLUMNNUMBER=OP.COLUMNNUMBER,
     FLDFMTCD=OP.FLDFMTCD,SIGNEDIND=OP.SIGNEDIND,
     STARTPOSITION=OP.STARTPOSITION,MAXLEN=OP.MAXLEN,
     ORDINALPOSITION=OP.ORDINALPOSITION,DECIMALCNT=OP.DECIMALCNT,
     ROUNDING=OP.ROUNDING,FLDCONTENTCD=OP.FLDCONTENTCD,
     JUSTIFYCD=OP.JUSTIFYCD,DEFAULTVAL=OP.DEFAULTVAL,
     VISIBLE=OP.VISIBLE,SUBTOTALTYPECD=OP.SUBTOTALTYPECD,
     SPACESBEFORECOLUMN=OP.SPACESBEFORECOLUMN,
     EXTRACTAREACD=OP.EXTRACTAREACD,
     EXTRAREAPOSITION=OP.EXTRAREAPOSITION,
     SUBTLABEL=OP.SUBTLABEL,RPTMASK=OP.RPTMASK,
     HDRJUSTIFYCD=OP.HDRJUSTIFYCD,HDRLINE1=OP.HDRLINE1,
     HDRLINE2=OP.HDRLINE2,HDRLINE3=OP.HDRLINE3,
     FORMATCALCLOGIC=OP.FORMATCALCLOGIC,
     CREATEDTIMESTAMP=OP.CREATEDTIMESTAMP,
     CREATEDUSERID=OP.CREATEDUSERID,
     LASTMODTIMESTAMP=OP.LASTMODTIMESTAMP,
     LASTMODUSERID=OP.LASTMODUSERID
    WHERE ENVIRONID=OP.ENVIRONID
    AND VIEWID=OP.VIEWID
    AND VIEWCOLUMNID=OP.VIEWCOLUMNID;
  END FOR;
  
 END IF;

END