CREATE PROCEDURE GETVIEWPROPS(IN P_ENVTID INT,
                                 P_ADMININD INT,
                                 P_GROUPID INT)
 DYNAMIC RESULT SETS 1
 LANGUAGE SQL
 DISABLE DEBUG MODE
 
BEGIN

 DECLARE T_CUR1 CURSOR WITH RETURN FOR
 
  SELECT 'VFOLDER',VIEWFOLDERID,NAME,3,''
  FROM VIEWFOLDER
  WHERE  ENVIRONID = P_ENVTID
  AND VIEWFOLDERID > 0

  UNION
      
  SELECT 'LR', LOGRECID, NAME,3,''
  FROM LOGREC
  WHERE ENVIRONID = P_ENVTID AND LOGRECID > 0

  UNION
   
  SELECT 'CONTROLREC', CONTROLRECID, NAME,3,''
  FROM CONTROLREC
  WHERE ENVIRONID = P_ENVTID AND CONTROLRECID > 0

  UNION
   
  SELECT DISTINCT 'LFPIPE', A.LOGFILEID, A.NAME,3,''
  FROM LOGFILE A, PHYFILE B, LFPFASSOC C
   WHERE A.ENVIRONID = B.ENVIRONID
   AND B.ENVIRONID   = C.ENVIRONID
   AND A.LOGFILEID   = C.LOGFILEID
   AND A.LOGFILEID   > 0
   AND B.PHYFILEID   = C.PHYFILEID
   AND A.ENVIRONID   = P_ENVTID AND A.LOGFILEID > 0

  UNION
   
  SELECT 'WRITEEXIT', EXITID, NAME,3,MODULEID
   FROM EXIT
   WHERE ENVIRONID = P_ENVTID
   AND EXITTYPECD ='WRITE' AND EXITID > 0 

  UNION
   
  SELECT 'FORMATEXIT',EXITID, NAME,3,MODULEID
   FROM EXIT
   WHERE ENVIRONID = P_ENVTID
   AND EXITTYPECD ='FORMT' AND EXITID > 0 ;

 DECLARE T_CUR2 CURSOR WITH RETURN FOR
 
  SELECT 'VFOLDER',A.VIEWFOLDERID, A.NAME,B.RIGHTS,''
  FROM VIEWFOLDER A
  LEFT OUTER JOIN SECVIEWFOLDER B
  ON  A.ENVIRONID   = B.ENVIRONID 
  AND B.GROUPID   = P_GROUPID
  AND A.VIEWFOLDERID = B.VIEWFOLDERID
  WHERE A.ENVIRONID  = P_ENVTID
  AND A.VIEWFOLDERID > 0

  UNION
      
  SELECT 'LR', A.LOGRECID, A.NAME,B.RIGHTS,''
  FROM LOGREC A
  INNER JOIN SECLOGREC B
  ON  A.ENVIRONID = B.ENVIRONID
  AND A.LOGRECID  = B.LOGRECID
  AND B.GROUPID = P_GROUPID
  WHERE A.ENVIRONID  = P_ENVTID AND A.LOGRECID > 0

  UNION
   
  SELECT 'CONTROLREC', CONTROLRECID, NAME,1,''
  FROM CONTROLREC
  WHERE ENVIRONID = P_ENVTID AND CONTROLRECID > 0

  UNION
   
  SELECT 'LFPIPE', S.LOGFILEID, S.NAME, D.RIGHTS,'' FROM
        (SELECT DISTINCT 'LFPIPE', A.ENVIRONID, A.LOGFILEID, A.NAME
  FROM LOGFILE A, PHYFILE B, LFPFASSOC C
  WHERE A.ENVIRONID = B.ENVIRONID
  AND B.ENVIRONID   = C.ENVIRONID
  AND A.LOGFILEID   = C.LOGFILEID
  AND A.LOGFILEID   > 0
  AND B.PHYFILEID = C.PHYFILEID
  AND A.ENVIRONID = P_ENVTID ) AS S LEFT OUTER JOIN
  SECLOGFILE D ON S.ENVIRONID=D.ENVIRONID AND
  S.LOGFILEID = D.LOGFILEID AND D.GROUPID = P_GROUPID

  UNION
   
  SELECT 'WRITEEXIT' , A.EXITID, A.NAME,B.RIGHTS,MODULEID
  FROM EXIT A
  LEFT OUTER JOIN SECEXIT B
  ON  A.ENVIRONID = B.ENVIRONID
  AND A.EXITID = B.EXITID
  AND B.GROUPID = P_GROUPID
  WHERE  A.ENVIRONID  = P_ENVTID
  AND EXITTYPECD ='WRITE' AND A.EXITID > 0

  UNION
   
  SELECT 'FORMATEXIT',A.EXITID, A.NAME,B.RIGHTS,MODULEID
  FROM EXIT A
  LEFT OUTER JOIN SECEXIT B
  ON  A.ENVIRONID = B.ENVIRONID
  AND A.EXITID = B.EXITID
  AND B.GROUPID = P_GROUPID
  WHERE A.ENVIRONID  = P_ENVTID
  AND EXITTYPECD ='FORMT' AND A.EXITID > 0 ;
  
IF P_ADMININD = 1 THEN

  OPEN T_CUR1 ;

ELSE
   
  OPEN T_CUR2 ;
    
END IF ;

END