CREATE PROCEDURE INSVIEWCOLSRC (
  IN DOC CLOB, 
  IN IMPORT SMALLINT)
  
RESULT SETS 0
LANGUAGE SQL
DISABLE DEBUG MODE 

BEGIN
 DECLARE RET INT DEFAULT 0;
 DECLARE PDOC XML;
 SET PDOC = XMLPARSE(DOCUMENT DOC PRESERVE WHITESPACE);

-- If normal create
 IF IMPORT = 0 THEN

   INSERT INTO VIEWCOLUMNSOURCE (
     ENVIRONID,VIEWCOLUMNID,
     VIEWSOURCEID,VIEWID,SOURCETYPEID,
     CONSTVAL,LOOKUPID,LRFIELDID,
     EFFDATEVALUE,EFFDATETYPE,EFFDATELRFIELDID,
     SORTTITLELOOKUPID,SORTTITLELRFIELDID,
     EXTRACTCALCLOGIC,
     CREATEDTIMESTAMP,CREATEDUSERID,
     LASTMODTIMESTAMP,LASTMODUSERID)
   SELECT X.ENVIRONID,X.VIEWCOLUMNID,
     X.VIEWSOURCEID,X.VIEWID,X.SOURCETYPEID,
     X.CONSTVAL,X.LOOKUPID,X.LRFIELDID,
     X.EFFDATEVALUE,X.EFFDATETYPE,X.EFFDATELRFIELDID,
     X.SORTTITLELOOKUPID,X.SORTTITLELRFIELDID,
     X.EXTRACTCALCLOGIC,
     CURRENT TIMESTAMP,X.CREATEDUSERID,
     CURRENT TIMESTAMP,X.LASTMODUSERID
   FROM XMLTABLE('$doc/Root/Record' passing PDOC as "doc"
   COLUMNS 
     "ENVIRONID"         INT PATH 'ENVIRONID',
     "VIEWCOLUMNID"      INT PATH 'VIEWCOLUMNID',
     "VIEWSOURCEID"      INT PATH 'VIEWSOURCEID',
     "VIEWID"            INT PATH 'VIEWID',
     "SOURCETYPEID"      INT PATH 'SOURCETYPEID',
     "CONSTVAL"          VARCHAR(255) PATH 'CONSTVAL',
     "LOOKUPID"          INT PATH 'LOOKUPID',
     "LRFIELDID"         INT PATH 'LRFIELDID',
     "EFFDATEVALUE"      VARCHAR(24) PATH 'EFFDATEVALUE',
     "EFFDATETYPE"       CHAR(5) PATH 'EFFDATETYPE',
     "EFFDATELRFIELDID"  INT PATH 'EFFDATELRFIELDID',
     "SORTTITLELOOKUPID" INT PATH 'SORTTITLELOOKUPID',     
     "SORTTITLELRFIELDID" INT PATH 'SORTTITLELRFIELDID',
     "EXTRACTCALCLOGIC"  CLOB(32 M) PATH 'EXTRACTCALCLOGIC',
     "CREATEDUSERID"     VARCHAR(8) PATH 'CREATEDUSERID',
     "LASTMODUSERID"     VARCHAR(8) PATH 'LASTMODUSERID') AS X;
 
 -- Creation from an import or migration 
  ELSE

   INSERT INTO VIEWCOLUMNSOURCE (
     ENVIRONID,VIEWCOLUMNSOURCEID,VIEWCOLUMNID,
     VIEWSOURCEID,VIEWID,SOURCETYPEID,
     CONSTVAL,LOOKUPID,LRFIELDID,
     EFFDATEVALUE,EFFDATETYPE,EFFDATELRFIELDID,
     SORTTITLELOOKUPID,SORTTITLELRFIELDID,
     EXTRACTCALCLOGIC,
     CREATEDTIMESTAMP,CREATEDUSERID,
     LASTMODTIMESTAMP,LASTMODUSERID)
   SELECT X.ENVIRONID,X.VIEWCOLUMNSOURCEID,X.VIEWCOLUMNID,
     X.VIEWSOURCEID,X.VIEWID,X.SOURCETYPEID,
     X.CONSTVAL,X.LOOKUPID,X.LRFIELDID,
     X.EFFDATEVALUE,X.EFFDATETYPE,X.EFFDATELRFIELDID,
     X.SORTTITLELOOKUPID,X.SORTTITLELRFIELDID,
     X.EXTRACTCALCLOGIC,
     X.CREATEDTIMESTAMP,X.CREATEDUSERID,
     X.LASTMODTIMESTAMP,X.LASTMODUSERID
   FROM XMLTABLE('$doc/Root/Record' passing PDOC as "doc"
   COLUMNS 
     "ENVIRONID"         INT PATH 'ENVIRONID',
     "VIEWCOLUMNSOURCEID" INT PATH 'VIEWCOLUMNSOURCEID',
     "VIEWCOLUMNID"      INT PATH 'VIEWCOLUMNID',
     "VIEWSOURCEID"      INT PATH 'VIEWSOURCEID',
     "VIEWID"            INT PATH 'VIEWID',
     "SOURCETYPEID"      INT PATH 'SOURCETYPEID',
     "CONSTVAL"          VARCHAR(255) PATH 'CONSTVAL',
     "LOOKUPID"          INT PATH 'LOOKUPID',
     "LRFIELDID"         INT PATH 'LRFIELDID',
     "EFFDATEVALUE"      VARCHAR(24) PATH 'EFFDATEVALUE',
     "EFFDATETYPE"       CHAR(5) PATH 'EFFDATETYPE',
     "EFFDATELRFIELDID"  INT PATH 'EFFDATELRFIELDID',
     "SORTTITLELOOKUPID" INT PATH 'SORTTITLELOOKUPID',     
     "SORTTITLELRFIELDID" INT PATH 'SORTTITLELRFIELDID',
     "EXTRACTCALCLOGIC"  CLOB(32 M) PATH 'EXTRACTCALCLOGIC',
     "CREATEDTIMESTAMP"  TIMESTAMP PATH 'CREATEDTIMESTAMP',
     "CREATEDUSERID"     VARCHAR(8) PATH 'CREATEDUSERID',
     "LASTMODTIMESTAMP"  TIMESTAMP PATH 'LASTMODTIMESTAMP',
     "LASTMODUSERID"     VARCHAR(8) PATH 'LASTMODUSERID') AS X;
  
  END IF;
SET RET=(SELECT IDENTITY_VAL_LOCAL() AS IDENTITY FROM SYSIBM.SYSDUMMY1);
RETURN RET;  
END